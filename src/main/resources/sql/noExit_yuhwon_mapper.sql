--AND TO_CHAR(RO.OPEN_AT, 'YYYY-MM-DD') = '2026-07-19'
--AND R.CAFE_ID = 1
ORDER BY CAFE_ID ASC, ROOM_ID ASC, OPEN_AT DESC
