          END AS PARTY_ROLE
    , CASE 
        WHEN VRA.CANCEL_AT IS NOT NULL THEN '예약 취소' 
        WHEN VRA.OPEN_AT < SYSDATE THEN  '플레이 완료'
        ELSE '예약 중' 
      END AS RES_STATUS
    , VRA.RES_OPEN_ID, VRA.OPEN_AT, VRA.BOOKED_AT
    , FN_GET_ATTEND_STATUS(VRA.RESERVATION_ID, 5) AS ATTEND_STATUS
    , CASE
        WHEN VRA.CANCEL_AT IS NULL THEN NULL
        WHEN FN_GET_USER_ROLE(VRA.RES_OPEN_ID, VRA.CANCEL_USER, VRA.CANCEL_AT) = 'USER' 
