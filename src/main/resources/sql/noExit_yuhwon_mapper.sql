        WHEN FN_GET_USER_ROLE(VRA.RES_OPEN_ID, VRA.CANCEL_USER, VRA.CANCEL_AT) IN ('OWNER', 'MANAGER')
            THEN '매장 취소'       
        ELSE '알 수 없음' 
            END AS CANCEL_TYPE
    , VRA.CANCEL_AT AS CANCELED_AT
    FROM VW_RESERVATION_ALL VRA
       -- LEFT JOIN RESERVATION_CANCEL RC
      --  ON VRA.RESERVATION_ID = RC.RESERVATION_ID
    WHERE VRA.LEADER_ID = 5 --로그인 한 사용자아이디
        OR VRA.PARTY_ID IN (
            SELECT PA.PARTY_ID
            FROM PARTY_MEMBER PM 
                JOIN PARTY_APPLY PA
                ON PM.APPLY_ID = PA.APPLY_ID
            WHERE PA.USER_ID = 5 -- 로그인 한 사용자 아이디
                AND NOT EXISTS(
                                SELECT PK.MEMBER_ID
                                FROM PARTY_KICK PK
                                WHERE PK.MEMBER_ID = PM.MEMBER_ID
                                )
        )
)
-- 분기 시 사용 
WHERE RES_STATUS = '플레이 완료'
ORDER BY OPEN_AT DESC
		OFFSET 0 ROWS
		FETCH FIRST 10 ROWS ONLY;
        
