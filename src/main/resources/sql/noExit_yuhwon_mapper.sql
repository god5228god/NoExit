SELECT *
FROM (
    SELECT 
        C.CAFE_NAME,
        R.ROOM_ID, 
        R.ROOM_NAME, 
        RG.GENRE_NAME,
        R.ROOM_IMG, 
        R.IS_ADULT, 
        R.CREATED_AT,
        (
            SELECT AVG(RV.SATISFACTION)
            FROM REVIEW RV
                JOIN ATTENDANCE_DETAIL AD ON RV.DETAIL_ID = AD.DETAIL_ID
                JOIN ATTENDANCE AT ON AD.ATTENDANCE_ID = AT.ATTENDANCE_ID
                JOIN RESERVATION RES ON AT.RESERVATION_ID = RES.RESERVATION_ID
                JOIN PARTY P ON RES.PARTY_ID = P.PARTY_ID
                JOIN PARTY_ROOM PR ON PR.PARTY_ID = P.PARTY_ID
                JOIN RES_OPEN RO ON PR.RES_OPEN_ID = RO.RES_OPEN_ID
            WHERE RO.ROOM_ID = R.ROOM_ID
        ) AS AVG_GRADE
    FROM ROOM R
    JOIN ROOM_GENRE RG 
        ON R.GENRE_ID = RG.GENRE_ID
    JOIN CAFE C 
        ON R.CAFE_ID = C.CAFE_ID
)
ORDER BY AVG_GRADE DESC NULLS LAST, CREATED_AT DESC
