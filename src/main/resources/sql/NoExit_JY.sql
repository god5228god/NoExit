
-- 1. 매너온도 구하는 함수
CREATE OR REPLACE FUNCTION FN_GET_MANNER(P_USER_ID NUMBER)
	RETURN NUMBER
IS 
    V_ORIGIN_TEMP NUMBER := 36.5;
	V_TEMP NUMBER;
    
BEGIN	
	SELECT NVL(SUM(SCORE),0)
	INTO V_TEMP
	FROM MANNER_HISTORY
	WHERE USER_ID = P_USER_ID;

	RETURN V_TEMP + V_ORIGIN_TEMP;
END;


