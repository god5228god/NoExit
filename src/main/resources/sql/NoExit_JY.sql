-- 7. 노쇼 매너온도 하락 트리거
CREATE OR REPLACE TRIGGER TRG_NOSHOW
AFTER INSERT ON ATTENDANCE_DETAIL
FOR EACH ROW
WHEN (NEW.ATTEND_STATUS_ID = 2)
BEGIN
    INSERT INTO MANNER_HISTORY(MANNER_HISTORY_ID, USER_ID, REASON_ID, SCORE, CREATED_AT)
    VALUES(MANNER_HISTORY_SEQ.NEXTVAL, :NEW.USER_ID,1,-1,SYSDATE);
END;


-- 8. 리뷰 작성 매너온도 상승 트리거
CREATE OR REPLACE TRIGGER TRG_REVIEW
AFTER INSERT ON REVIEW
FOR EACH ROW
DECLARE
    V_USER_ID NUMBER;
BEGIN

    SELECT A.USER_ID INTO V_USER_ID
    FROM ATTENDANCE_DETAIL A
    WHERE A.ATTEND_STATUS_ID = 1
    AND A.DETAIL_ID = :NEW.DETAIL_ID;

    INSERT INTO MANNER_HISTORY(MANNER_HISTORY_ID, USER_ID, REASON_ID, SCORE, CREATED_AT)
    VALUES(MANNER_HISTORY_SEQ.NEXTVAL, V_USER_ID,4,0.1,SYSDATE);
    
EXCEPTION
    WHEN NO_DATA_FOUND THEN
    NULL;
    
END;

-- 9. 상호평가 3회 매너온도 상승 트리거
CREATE OR REPLACE TRIGGER TRG_EVAL
AFTER INSERT ON MUTUAL_EVALUATION
FOR EACH ROW
DECLARE
    V_USER_ID NUMBER;
    V_COLUMN_COUNT NUMBER;
BEGIN
    SELECT A.USER_ID INTO V_USER_ID
    FROM ATTENDANCE_DETAIL A
    WHERE A.ATTEND_STATUS_ID = 1
    AND A.DETAIL_ID = :NEW.DETAIL_ID;
    
    SELECT COUNT(*) AS COUNT INTO V_COLUMN_COUNT
    FROM ATTENDANCE_DETAIL A
    WHERE A.ATTEND_STATUS_ID = 1
    AND A.DETAIL_ID = :NEW.DETAIL_ID;
    
    
    IF MOD(COUNT, 6) == 0 THEN 
    INSERT INTO MANNER_HISTORY(MANNER_HISTORY_ID, USER_ID, REASON_ID, SCORE, CREATED_AT)
    VALUES(MANNER_HISTORY_SEQ.NEXTVAL, V_USER_ID,2,0.2,SYSDATE);
    
END;





